//Every tick call, duty from feedback
void HAL_TIM_Update()
{
	static uint8_t i = 0, j = 1;
	static uint16_t calc_pwm=0;
	if(duty<10)duty=10;
	if(duty>1990)duty=1990;
	calc_pwm = duty * sinus_table[i];
	if(j)
		{
			TIM1->CCR1 = 2000+calc_pwm;
			TIM1->CCR2 = 2000-calc_pwm;
		}
	else
		{
			TIM1->CCR1 = 2000-calc_pwm;
			TIM1->CCR2 = 2000+calc_pwm;
		}
	i++;
	if(i > 179)
		{
			i = 0;
			j = !j;
		}
}
